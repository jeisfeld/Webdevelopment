<h2>
	<span class="mobile">Zufallsbild - </span>Einstellungen
</h2>

<h3>App-Einstellungen</h3>

<p>
	Während Sie sich auf der Haupt-Konfigurationsseite befinden, können Sie auf das Symbol <img
		src="<?=$basepath?>/drawable/ic_action_settings.png" /> drücken, um zur Einstellungsseite zu gelangen. Hier finden Sie
	folgende Kategorien von Einstellungen:
</p>

<h4>Premium-Version</h4>

<p>Hier können Sie die App &bdquo;Zufallsbild Pro&ldquo; erwerben, die die Verwendung von mehr als drei Bildlisten in
	der App ermöglicht.</p>

<h4>Unterstützung</h4>

<p>Hier können Sie diese Hilfe-Seite öffnen oder den Entwickler per E-Mail kontaktieren.</p>

<h4>
	<a name="randomImageView"></a>
	Zufallsbild-Ansicht
</h4>

<ul>
	<li><b>Bildskalierung:</b> Hier können Sie einstellen, wie die Fotos bei der Anzeige skaliert werden sollen.</li>
	<li><b>Hintergrundfarbe:</b> Hier können Sie einstellen, welche Hintergrundfarbe angezeigt werden soll.</li>
	<li><b>Verhalten beim Schnippen:</b> Hier können Sie angeben, was passiert, wenn sie das angezeigte Bild wegschnippen.
		<ul>
			<li><b>Immer neues Zufallsbild:</b> Es wird wieder zufällig ein neues Bild ausgewählt</li>
			<li><b>Ein Bild rückwärts möglich:</b> Analog, aber man kann ein Bild zurückgehen</li>
			<li><b>Mehrere Bilder rückwärts möglich:</b> Analog, aber man kann mehrere Bilder zurückgehen</li>
			<li><b>Rückwärts möglich, Wiederholungen vermeiden:</b> Analog, aber bei der auswahl des nächsen Zufallsbildes werden
				Wiederholungen vermieden</li>
			<li><b>Alle Bilder zyklisch zeigen:</b> Alle Bilder der Liste werden zyklisch gezeigt</li>
			<li><b>Keine Änderung:</b> Schnippen hat keine Auswirkung</li>
			<li><b>Ansicht schließen:</b> Schnippen schließt die Ansicht</li>
		</ul></li>
	<li><b>Automatische Wechselhäufigkeit:</b> Hier können Sie festlegen, ob und wie oft das Bild automatisch wechseln
		soll.</li>
	<li><b>Tippen statt Schnippen:</b> Hier können Sie festlegen, ob das Bild durch einfaches Tippen statt durch Schnippen
		gewechselt werden kann.</li>
	<li><b>Bildschirmtimeout verhindern:</b> Hier können Sie festlegen, ob der Timeout des Bildschirms verhindert werden
		soll, während Sie Bilder anschauen.</li>
</ul>

<h4>Weitere Einstellungen</h4>

<ul>
	<li><b>Verzeichnisauswahl:</b> Hier können Sie festlegen, auf welche Art Sie Bilder und Verzeichnisse zum Hinzufügen
		auswählen:
		<ul>
			<li><b>Liste aller Bildverzeichnisse (Voreinstellung):</b> Hier wird Ihnen die Liste aller Bildverzeichnisse
				angezeigt, und Sie können aus dieser Liste auswählen. Dies ist die einfachste Option.</li>
			<li><b>Dateibrowser:</b> Hier können Sie Ihre Bildverzeichnisse in einem Dateibrowser auswählen.</li>
			<li><b>Galerie:</b> Hier können Sie die Bildverzeichnisse in der Galerie-App auswählen. Dies kann hilfreich sein,
				wenn Sie Ihre Bilder in der Galerie am einfachsten finden.</li>
		</ul></li>
	<li><b>Bildverzeichnisse suchen:</b> Hier können Sie eine Durchsuchung des Gerätes nach Bildverzeichnissen veranlassen.</li>
	<li><b>Bevorzugte Bildordner (ab Android 10):</b> Hier können Sie Bildorder auswählen, die von der App immer durchsucht
		werden sollen, wenn Sie Bildlisten bearbeiten.</li>
	<li><b>Sicherungsordner zurücksetzen (ab Android 10):</b> Hier können Sie die Auswahl des Sicherungsordners rückgängig
		machen, so dass er bei der nächsten Sicherung / Wiederherstellung erneut abgefragt wird.</li>
	<li><b>Versteckte Verzeichnisse anzeigen (nur bis Android 9):</b> Hier können Sie festlegen, dass auch Bilder aus
		Verzeichnissen berücksichtigt werden, die versteckt sind.</li>
	<li><b>Apps ohne Popup-Benachrichtigungen:</b> Hier können Sie Apps auswählen, bei deren Ausführung keine
		Popup-Notifications kommen sollen (Android 5 oder höher).</li>
	<li><b>Sprache:</b> Hier können Sie die Sprache der App ändern. (Derzeit unterstützt: Englisch, Deutsch und Spanisch)</li>
	<li><b>Benachrichtigung bei Listenänderung:</b> Hier können Sie festlegen, dass eine Benachrichtigung angezeigt wird,
		wenn Sie etwas zu einer Bildliste hinzufügen oder entfernen.</li>
	<li><b>Regexp-Filter verwenden:</b> Dies erlaubt, gewisse Listen oder Verzeichnisse generell aus der Anzeige
		herauszufiltern.</li>
	<li><b>Alle Hinweise zeigen:</b> Es gibt verschiedene Informations-Dialoge in der App, die Sie durch Drücken der Taste
		&bdquo;Nicht mehr zeigen&ldquo; permanent deaktivieren können. Die Schaltfläche &bdquo;Alle Hinweise zeigen&ldquo;
		aktiviert all diese Nachrichten wieder.</li>
	<li><b>Hinweise nicht mehr zeigen:</b> Dies deaktiviert alle Arten von Info-Meldungen, die sonst individuell
		ausgeschaltet werden müssen. Dies ist hilfreich für erfahrene Nutzer, die die Anwendung auf einem Gerät installiert
		haben.</li>
</ul>

<h3>
	<a name="widgetSettings"></a>
	Widget-Einstellungen
</h3>

<ul>
	<li><b>Listenname:</b> Hier können Sie einstellen, welche Bildliste das Widget verwenden soll.</li>
	<li><b>Bildliste bearbeiten:</b> Dieser Link verweist zur Bearbeitung der Bildliste, für die das Widget konfiguriert
		ist.</li>
	<li><b>Name des Widgets:</b> Hier können Sie einen Namen angeben, der in der Widget-Konfiguration angezeigt wird.</li>
	<li><b>Icon des Widgets:</b> Für das Mini-Widget können Sie hier ein Icon-Bild auswählen.</li>
	<li><b>Wechselhäufigkeit:</b> Hier können Sie einstellen, wie häufig das Bild wechseln soll.</li>
	<li><b>Als Liste anzeigen:</b> Beim Zufallsbilderstapel kann hier eingestellt werden, dass die Bilder nicht als Stapel
		sondern als Liste angezeigt werden.</li>
	<li><b>Alle Bilder zyklisch anzeigen:</b> Beim Zufallsbilderstapel kann hier eingestellt werden, ob hier alle Bilder
		der Liste in den Stapel kommen, oder ob anhand der Gewichtungen nur einige Bilder aufgenommen werden (was bei sehr
		großen Bildersammlungen empfehlenswert ist).</li>
	<li><b>Hintergrund:</b> Hier können Sie die Hintergrundfarbe des Widgets festlegen. Die Auswahl &bdquo;Rahmen mit Bild
		füllen&ldquo; bedeutet, dass das Bild gestreckt wird, um das ganze Widget auszufüllen.</li>
	<li><b>Button-Stil:</b> Hier können Sie einstellen, wo die zwei Widget-Buttons angezeigt werden. Dies sind:
		<ul>
			<li>Ein Button zum ändern der Settings</li>
			<li>Ein Button, um das angezeigte Bild zu wechseln</li>
		</ul></li> &bdquo;Versteckt&ldquo; bedeutet, dass die Buttons nicht angezeigt werden, aber Sie können auf den rechten
	und linken Rand des Widgets tippen.
	<li><b>Button-Farbe:</b> Hier können Sie einstellen, welche Farbe diese beiden Buttons haben sollen.</li>
	<li><b>Standardeinstellungen verwenden:</b> Hier können Sie festlegen, ob beim Wechsel in die Detailansicht die
		allgemeinen Standardeinstellungen gelten, oder ob die Einstellungen &bdquo;<b>Bildskalierung</b>&ldquo;, &bdquo;<b>Hintergrundfarbe</b>&ldquo;,
		&bdquo;<b>Verhalten beim Schnippen</b>&ldquo;, &bdquo;<b>Automatische Wechselhäufigkeit</b>&ldquo;, &bdquo;<b>Tippen
			statt Schnippen</b>&ldquo; und &bdquo;<b>Bildschirmtimeout verhindern</b>&ldquo; für dieses Widget separat festgelegt
		werden sollen. (Erläuterung zu diesen Einstellungen siehe <a href="#randomImageView">oben</a>.)</br> Beim Tippen des
		Mini-Widgets wird das zuletzt angezeigte Bild erneut angezeigt. Wenn Sie immer gleich ein neues Bild sehen wollen,
		können Sie bei &bdquo;Verhalten beim Schnippen&ldquo; den Wert &bdquo;Immer neues Zufallsbild&ldquo; wählen.</li>
	<li><b>Nutzungsdauer des Widgets:</b> Hier können Sie beschränken, wie lange Sie Bilder über das Mini-Widget anschauen
		können.</li>
	<li><b>Sperrdauer des Widgets:</b> Hier können Sie beschränken, wie oft sie das Mini-Widget nutzen können.</li>
</ul>

<h3>
	<a name="notificationSettings"></a>
	Benachrichtigungs-Einstellungen
</h3>

<ul>
	<li><b>Listenname:</b> Hier können Sie einstellen, welche Bildliste die Benachrichtigung verwenden soll.</li>
	<li><b>Bildliste bearbeiten:</b> Dieser Link verweist zur Bearbeitung der Bildliste, für die die Benachrichtigung
		konfiguriert ist.</li>
	<li><b>Name der Benachrichtigung:</b> Hier können Sie einen Namen angeben, der in der Benachrichtigungs-Konfiguration
		angezeigt wird.</li>
	<li><b>Häufigkeit der Benachrichtigung:</b> Hier können Sie einstellen, wie häufig eine Benachrichtigung angezeigt
		werden soll.</li>
	<li><b>Variabilität der Benachrichtigung:</b> Hier können Sie wählen, ob die Benachrichtigungen ungefähr in gleichen
		Abständen oder in größerer Variabilität kommen sollen.</li>
	<li><b>Startzeit der Benachricheigungen, Endzeit der Benachrichtigungen:</b> Hier können Sie ein Zeitfenster angeben,
		in dem die Benachrichtigungen kommen können.</li>
	<li><b>Anzeigedauer:</b> Hier können Sie festlegen, die Benachrichtigungen nach einer gewissen Zeit automatisch zu
		löschen.</li>
	<li><b>Benachrichtigungsstil:</b> Hier können Sie zwischen fünf Arten von Benachrichtigungen wählen.</li>
	<li><b>LED-Farbe:</b> Hier können Sie festlegen, ob die LED zur Benachrichtigung blinkt.</li>
	<li><b>Vibration:</b> Hier können Sie festlegen, ob das Gerät bei der Benachrichtigung vibriert.</li>
	<li><b>Farbiges Symbol:</b> Hier können Sie festlegen, ob das Benachrichtigungssymbol farbig oder weiß sein soll.</li>
	<li><b>Standardeinstellungen verwenden:</b> Hier können Sie festlegen, ob beim Wechsel in die Detailansicht die
		allgemeinen Standardeinstellungen gelten, oder ob die Einstellungen &bdquo;<b>Bildskalierung</b>&ldquo;, &bdquo;<b>Hintergrundfarbe</b>&ldquo;,
		&bdquo;<b>Verhalten beim Schnippen</b>&ldquo;, &bdquo;<b>Automatische Wechselhäufigkeit</b>&ldquo;, &bdquo;<b>Tippen
			statt Schnippen</b>&ldquo; und &bdquo;<b>Bildschirmtimeout verhindern</b>&ldquo; für diese Benachrichtigung separat
		festgelegt werden sollen. (Erläuterung zu diesen Einstellungen siehe <a href="#randomImageView">oben</a>.)</li>
	<li><b>Benachrichtigung entfernen:</b> Hier können Sie die Benachrichtigung entfernen.</li>
</ul>
