<h2>Imagen Aleatoria - Información general</h2>

<p>¿Tiene muchas fotos digitales, pero no tiene tiempo para verlas? Entonces esta aplicación puede ayudarle.</p>

<p>Esta aplicación le permite seleccionar una cantidad arbitraria de imágenes y carpetas que están almacenadas en su
	teléfono inteligente (o tableta, incluida la tarjeta SD) en una lista, y luego, cuando lo desee, mostrar una foto al
	azar de su lista.</p>

<p>Puede configurar varias listas, p. Ej.</p>
<ul>
	<li>fotos de su socio</li>
	<li>fotos de sus vacaciones</li>
	<li>fotos de sus hijos cuando eran pequeños</li>
</ul>

<p>Luego, puede mostrar sus fotos de varias maneras:</p>
<ul>
	<li>A través de un widget en su pantalla de inicio que muestra regularmente una nueva imagen aleatoria</li>
	<li>A través de un icono de pantalla de inicio que le permite mostrar una imagen aleatoria bajo demanda</li>
	<li>A través de notificaciones que aparecen en intervalos variables</li>
</ul>

<p>Simplemente pruebe la aplicación y pruebe sus posibilidades.</p>

<h3>Caracteristicas</h3>

<p>La aplicación incluye las siguientes características:</p>
<ul>
	<li>Configuración de listas de imágenes.</li>
	<li>Mostrar una foto arbitraria de la lista de imágenes</li>
	<li>Ampliar la imagen mostrada o cambiar a otra imagen aleatoria</li>
	<li>Colocar widgets en su pantalla de inicio que muestren nuevas imágenes de su lista en intervalos de tiempo
		regulares</li>
	<li>Configuración de notificaciones que muestran imágenes de su lista en momentos aleatorios</li>
	<li>Agregar &laquo;listas anidadas&raquo; a sus listas de imágenes y asígneles peso: esto le permite configurar
		imágenes &laquo;raras&raquo; para que se muestren con más frecuencia.</li>
	<li>Copia de seguridad y restauración de listas de imágenes</li>
</ul>

<p>La aplicación está limitada a tres listas de imágenes. Si le gusta la aplicación, puede comprar una versión
	premium que no tiene esta limitación (como aplicación separada).</p>

