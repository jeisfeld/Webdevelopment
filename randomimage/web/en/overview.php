<h2>Random Image - Overview</h2>

<p>You have lots of great digital photos, but no time so see them? Then this app may help you.</p>

<p>This app allows you to select an arbitrary number of images and folders that are stored on your smartphone (or
	tablet, including SD card) into a list, and then, whenever you want, to get one random photo out of your list
	displayed.</p>

<p>You may configure multiple lists, e.g.</p>
<ul>
	<li>photos of your sweetheart</li>
	<li>photos of your vacations</li>
	<li>photos of your children when they were young</li>
</ul>

<p>Then you may display your photos in various ways:</p>
<ul>
	<li>Via a widget on your homescreen that regularly displays a new random image</li>
	<li>Via a home screen icon that allows you to display a random image on demand</li>
	<li>Via notifications that appear in varying intervals</li>
</ul>

<p>Just test the app and try out its possibilities.</p>

<h3>Features</h3>

<p>The app includes the following features:</p>
<ul>
	<li>Configuration of image lists</li>
	<li>Display an arbitrary photo from the image list</li>
	<li>Zoom the displayed image or switch to another Random image</li>
	<li>Place widgets on your home screen that display new images from your list in regular time intervals</li>
	<li>Configuration of notifications that display images from your list at random times</li>
	<li>Add &ldquo;nested lists&rdquo; to your image lists and give weights to them - this allows you to configure
		&ldquo;rare&rdquo; images to be displayed more frequently</li>
	<li>Backup and restore of image lists</li>
</ul>

<p>The app is limited to three image lists. If you like the app, you can purchase a premium version which does not
	have this limitation (as separate app).</p>

