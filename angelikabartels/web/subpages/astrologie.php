<h1>Psychologische Astrologie</h1>
<p class="subtitle">Die Königin der Weisheitslehre</p>

<p>
	<img src="<?=$basepath?>/img/astro_baum.jpg" class="astroimg">Astro&shy;logie ist die Lehre von den Ur-Prinzipien, von den
	Ur-Ideen, von den Arche&shy;typen dieses Universums. Die Himmels&shy;ebene oder kosmische Ebene ist nur eine Ebene,
	eine Ebene von vielen, auf der die Prinzipien und ihr Zusammen&shy;spiel gesehen und erkannt werden können.
</p>
<p>Die Planeten selbst beeinflussen das Geschehen hier in keiner Weise. Sie sind lediglich das kosmische Abbild dessen,
	was sich auf der Erde ereignet nach dem Prinzizp: &bdquo;Wie oben so unten&ldquo;. Das Horoskop bildet, symbolisch
	verschlüsselt, die Gesetzmäßigkeit ab, unter denen der Mensch, ein Tier, eine Nation, ein Ereignis und vieles mehr
	geboren werden.</p>
<p>Mit Hilfe der psychologischen Astrologie können wir Einsicht nehmen in unser Lebensprogramm und eine konstruktive
	Lebensgestaltung vornehmen, unsere Fähigkeiten und Talente entwickeln.</p>
<p>Wir wissen, Charakter + Zeit = Schicksal. Eine wichtige Formel, denn daran kann man begreifen lernen, dass diese
	ungewisse Zukunft, an die viele Menschen glauben, vom Menschen mitbestimmt werden kann. Es gibt keinen Zufall.</p>
<p>Diese Selbstbestimmung im Schicksalslauf fängt bei den Gedanken an, aus denen Bilder geformt werden und schließlich
	Handlungen entstehen. Diese Handlungen sind die Saat, und deren Ernte ist die Frucht, das Schicksal.</p>
<p>Es gelingt uns oft, unsere Wünsche und Hoffnungen zum Ausdruck zu bringen. Was fehlt, ist die andere Hälfte. Wir
	müssen herausfinden, woraus dieser fehlende Teil besteht.</p>

<p class="center">
	Was ist zu tun?<br>Was ist zu erkennen?<br>Das ist das Grundprinzip der Astrologie.
</p>

