		Michael Newton: &bdquo;Erinnerung aus dem Zwischenreich&ldquo;)</i>
</p>
<p>Durch die Erfahrung einer Rückführung in Vor- und Zwischenleben können die Klienten auf vielen Ebenen Heilung
	erfahren und ein tieferes Verständnis erlangen. Die Rückführung ins Zwischenleben lässt uns auf einer tieferen Ebene
	all die unbewussten Barrieren erkennen, die uns davon abhalten, ein glückliches und von unserer Seele geleitetes Leben
	zu führen.</p>

<p class="smallspace">Es ist eine Therapie, die uns aus dem Schleier des Vergessens herausführt. Sie führt uns über die
	Alterregression in ein wichtiges Vorleben für dieses Leben. Von dort aus geht die erhabene Reise ins Zwischenreich, wo
	wir unseren Seelenführer, unsere Seelengruppe begegnen. Hier können die bedeutungsvollen Fragen gestellt werden, die
	unser jetziges Leben betreffen, wie zum Beispiel:</p>
<ul class="smallspace">
	<li>Klärung meiner Lebensaufgabe, Tätigkeit und Lektionen</li>
	<li>Klärung von Beziehungen</li>
	<li>Mehr Leichtigkeit, Lebensfreude, Vertrauen in den Lebensfluss</li>
	<li>Mehr Selbstvertrauen, Selbstannahme und Selbstliebe, letztendlich Selbstheilung</li>
	<li>Welche Lektionen und Ziele hat sich meine Seele für dieses Leben vorgenommen?</li>
	<li>Was ist meine größte Angst, mein tiefstes Schuld- und Unsicherheitsgefühl, meine schwerste Verletzung?</li>
	<li>Was ist die Ursache meiner Angst die mich davon abhält, meine Fähigkeiten und Kräfte für das Wohl anderer und für
		mich selbst einzusetzen?</li>
	<li>Was kann ich für meine Kinder, für andere Menschen tun?</li>
</ul>
<p>Es ist eine einzigartige Gelegenheit dem großen Ganzen zu dienen.</p>

