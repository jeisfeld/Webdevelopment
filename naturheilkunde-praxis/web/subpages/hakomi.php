<p>Diese erfahrungs&shy;orientierte Körper&shy;psycho&shy;therapie arbeitet tiefen&shy;psycho&shy;logisch und
	körper&shy;orientiert. Während die Wahrnehmung verlangsamt und in innerer Achtsamkeit nach innen gerichtet wird, lässt
	sich das Zusammen&shy;spiel von Gefühlen, Gedanken und Körper&shy;reaktionen erforschen. Wir entdecken unsere ureigene
	Art und Weise, die Welt zu erleben: Tiefe Über&shy;zeugungen und einengende Glaubens&shy;muster können erkannt und neue
	Einsichten gewonnen und praktiziert werden. Die Hakomi-Therapie ist von der Haltung getragen, dass jedem Menschen eine
	Innere Weisheit innewohnt, die seine Selbst&shy;heilungs&shy;kräfte und Ressourcen aktivieren kann.</p>
<p>Diese Methode kann hilfreich unterstützen</p>
<ul>
	<li>bei körperlichen Beschwerden</li>
	<li>in Lebens- oder Beziehungs&shy;krisen</li>
	<li>bei emotionalen Schwierig&shy;keiten wie Ängsten, Trauer und Traumata</li>
	<li>bei allem, was sich nach Entfaltung sehnt und noch keinen Raum in Leben hat</li>
</ul>
