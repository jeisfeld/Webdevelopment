				Akupunktur&shy;punkte mit der jeweiligen Farbe stimuliert. Zusätzlich wird der ganze Körper mit Farblicht bestrahlt.
				Die aktuell wirkungs&shy;vollste Farbe wird intuitiv oder mit Hilfe der Puls&shy;testung ausgewählt.</p>

