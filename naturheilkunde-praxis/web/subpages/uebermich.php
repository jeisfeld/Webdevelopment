<p>Bereits 1984 begann mein natur&shy;heilkundlich geprägter Weg. Als Assistentin bei dem Natur&shy;heilarzt Dr. Seemann
	in Aalen lernte ich damals Bioresonanz, Elektro&shy;akupunktur nach Voll (EAV), Farblicht-Therapie nach Peter Mandel
	und weitere alternativ medizinische Verfahren kennen. Nach 4 Jahren führte mein Weg 1988 zur 3 jährigen Ganztags
	Heil&shy;praktiker Schule nach München. Während dieser Zeit assistierte ich bei dem Natur&shy;heilarzt Dr. Sachsse bis
	1997. Sein Schwerpunkt war die Bioresonanz, Allergie und seine Testmethode der Pulsreflex nach Nogier, die ich bis
	heute erfolgreich anwende.</p>
<p>Die Einflüsse dieser beiden Ärzte/Mentoren, die bis zu ihrem letzten Atemzug in der Praxis tätig waren, prägen meine
	Arbeit bis heute. Meine persönlichen Schwerpunkte, die Chinesische Medizin, Psycho&shy;somatik und die Spagyrik flossen
	mit in diese Arbeit ein. Letztere ist im tieferen Sinn eine Ordnungs&shy;therapie, die wie ein Magnet eine ordnende und
	ausrichtende Wirkung hat. Doch je weiter ich in das Ursachen-/Wirkungs&shy;prinzip eindrang, umso deutlicher wurde das
	Psychische, Seelische im Somatischen. Deshalb begann ich 1994 die dreijährige Hakomi-Ausbildung, um ein tieferes
	Verständnis von unseren Grund&shy;anschauungen etc. zu bekommen. Da bei dieser Methode dem Körper so eine besondere
	Bedeutung zukommt, passt sie sehr gut in meine ganzheitliche Behandlungs&shy;methode.</p>
