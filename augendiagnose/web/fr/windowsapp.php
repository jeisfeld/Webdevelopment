<h2>
	<span class="mobile"><?=$appname?> - </span>L'application Windows
</h2>

<p>Il existe une application Windows complémentaire qui permet de visualiser les photos des yeux sur un ordinateur
	Windows comme dans l'application Android.</p>

<p>L'application Windows est uniquement destinée comme complément à l'application Android. Elle offre moins de
	fonctionnalités que l'application Android. En particulier, elle ne permet pas d'organiser de nouvelles photos ni de
	définir la position des superpositions. Cependant, elle permet d'afficher les photos, d'afficher les superpositions et
	d'éditer le commentaire.</p>

<p>
	Pour utiliser l'application Windows, vous devez synchroniser le dossier de photos oculaires de votre appareil Android
	avec un dossier correspondant sur votre ordinateur Windows. Ce dossier doit être configuré dans les paramètres.<br> Une
	façon possible de synchronisation est d'installer une application WebDAV sur votre appareil Android et d'installer une
	application de synchronisation sur votre ordinateur Windows (comme FreeFileSync). Vous pouvez alors configurer de
	manière à ce que l'application de synchronisation accède sans fil à votre appareil Android via WebDAV.
</p>

<h3>Entrées de menu</h3>
<ul>
	<li><b>Fichier&rarr;Quitter :</b> Quitter l'application.</li>

	<li><b>Affichage&rarr;Volet des superpositions :</b> Afficher ou masquer le volet pour afficher les superpositions ou
		modifier la luminosité/le contraste.</li>

	<li><b>Affichage&rarr;Volet des commentaires :</b> Afficher ou masquer le volet pour éditer le commentaire.</li>

	<li><b>Affichage&rarr;Diviser la fenêtre :</b> Diviser la fenêtre pour que deux photos puissent être visualisées en
		parallèle.</li>

	<li><b>Fenêtre&rarr;Fermer :</b> Ici, vous pouvez fermer la vue détaillée de l'œil. (Alternativement, vous pouvez
		cliquer sur la croix en haut à droite.)</li>

	<li><b>Fenêtre&rarr;Paramètres :</b> Modifier les paramètres.</li>

	<li><b>Aide&rarr;Rechercher des mises à jour :</b> Ici, vous pouvez vérifier s'il existe une mise à jour de
		l'application. S'il y a une nouvelle mise à jour, vous en serez également informé au démarrage de l'application.</li>

	<li><b>Aide&rarr;Désinstaller l'application :</b> Ici, vous pouvez désinstaller l'application.</li>
</ul>

<h3>Paramètres</h3>

<ul>
	<li><b>Dossier des photos oculaires :</b> Ici, vous devez sélectionner le dossier principal où vous stockez les photos
		des yeux. Ce dossier doit avoir la même structure que dans l'application Android.</li>

	<li><b>Taille maximale du bitmap :</b> C'est la taille à laquelle les photos sont réduites avant l'affichage.</li>

	<li><b>Taille des miniatures :</b> C'est la taille à laquelle les photos sont réduites dans la vue des miniatures.</li>

	<li><b>Couleur par défaut des superpositions :</b> Ici, vous pouvez définir la couleur par défaut pour les
		superpositions de topographie de l'iris.</li>

	<li><b>Trier par nom de famille :</b> Ici, vous pouvez décider que les noms ne sont pas ordonnés strictement par ordre
		alphabétique tel que donné, mais par le nom de famille.</li>

	<li><b>Mettre à jour automatiquement :</b> Si vous sélectionnez ceci, les nouvelles versions de l'application seront
		installées automatiquement au démarrage.</li>

	<li><b>Langue :</b> Ici, vous pouvez changer la langue dans laquelle l'application est affichée. Un changement de
		valeur force un redémarrage de l'application.</li>
</ul>

